import UIKit

extension UIColor {
    struct Custom {
        static let DarkGrey = UIColor(red: 0.157, green: 0.153, blue: 0.173, alpha: 1)
        static let Black = UIColor(red:  0.106, green: 0.098, blue: 0.118, alpha: 1)
        static let LightGrey = UIColor(red: 0.775, green: 0.775, blue: 0.775, alpha: 1)
            static let FadedWhite = UIColor(red: 233, green: 233, blue: 233, alpha: 1)
        }
}
